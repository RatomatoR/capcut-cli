import { spawnSync } from "node:child_process";
import { writeFileSync } from "node:fs";

const result = spawnSync(process.execPath, ["dist/index.js", "describe"], {
  encoding: "utf-8",
  maxBuffer: 16 * 1024 * 1024,
});
if (result.status !== 0) throw new Error(result.stderr || `describe exited ${result.status}`);
const schema = JSON.parse(result.stdout);

writeFileSync("docs/command-reference.json", `${JSON.stringify(schema, null, 2)}\n`);
const rows = schema.commands.map(
  (command) =>
    `| \`${command.name}\` | \`${command.usage.replaceAll("|", "\\|")}\` | ${command.mutates ? "yes" : "no"} | ${command.summary} |`,
);
const markdown = [
  "# Generated command reference",
  "",
  "> Generated by `npm run docs:commands` from the v2 command registry. Do not edit manually.",
  "",
  "| Command | Usage | Mutates | Summary |",
  "|---|---|:---:|---|",
  ...rows,
  "",
].join("\n");
writeFileSync("docs/command-reference.md", markdown);
